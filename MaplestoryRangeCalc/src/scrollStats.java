
public class scrollStats {
	
	private int atk;
	private int mAtk;
	
	private int hp;
	private int str;
	private int dex;
	private int intl;
	private int luk;
	
	private int def;

	public scrollStats(int atk, int mAtk, int hp, int str, int dex, int intl, int luk, int def) {
		super();
		this.atk = atk;
		this.mAtk = mAtk;
		this.hp = hp;
		this.str = str;
		this.dex = dex;
		this.intl = intl;
		this.luk = luk;
		this.def = def;
	}

	public int getAtk() {
		return atk;
	}

	public void setAtk(int atk) {
		this.atk = atk;
	}

	public int getmAtk() {
		return mAtk;
	}

	public void setmAtk(int mAtk) {
		this.mAtk = mAtk;
	}

	public int getHp() {
		return hp;
	}

	public void setHp(int hp) {
		this.hp = hp;
	}

	public int getStr() {
		return str;
	}

	public void setStr(int str) {
		this.str = str;
	}

	public int getDex() {
		return dex;
	}

	public void setDex(int dex) {
		this.dex = dex;
	}

	public int getIntl() {
		return intl;
	}

	public void setIntl(int intl) {
		this.intl = intl;
	}

	public int getLuk() {
		return luk;
	}

	public void setLuk(int luk) {
		this.luk = luk;
	}

	public int getDef() {
		return def;
	}

	public void setDef(int def) {
		this.def = def;
	}
	
	

}
